#include<iostream>
using namespace std;
int main()
{
	int a,b;
	a=20,b=10;  //assignment operator

    
    //ARITHMETIC OPERATORS:
    
    cout<<"Addition: "<<a+b<<endl;
    cout<<"Subtraction: "<<a-b<<endl;
    cout<<"Multiplication: "<<a*b<<endl;
    cout<<"Division: "<<a/b<<endl;
    cout<<"Modulus: "<<a%b<<endl;
	
   //INCREMENT OPERATOR:
    a++;
	cout<<"Value of a "<<a<<endl;
	//DECREMENT OPERATOR:
	a--;
	cout<<"Value of a "<<a<<endl;
	return 0;	
	
}
